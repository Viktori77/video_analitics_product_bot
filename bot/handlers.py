def register_handlers(dp, bot_instance):
    """–†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –≤—Å–µ—Ö –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤"""
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º bot_instance –≤ –¥–∞–Ω–Ω—ã—Ö –¥–∏—Å–ø–µ—Ç—á–µ—Ä–∞
    dp['bot_instance'] = bot_instance
    
    @router.message()
    async def handle_text_message(message: Message, state: FSMContext):
        """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ —Ç–µ–∫—Å—Ç–æ–≤—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π"""
        try:
            # –ü–æ–ª—É—á–∞–µ–º bot_instance –∏–∑ –¥–∞–Ω–Ω—ã—Ö –¥–∏—Å–ø–µ—Ç—á–µ—Ä–∞
            bot_instance = dp['bot_instance']
            
            user_query = message.text.strip()
            logger.info(f"–ü–æ–ª—É—á–µ–Ω –∑–∞–ø—Ä–æ—Å: {user_query}")
            
            if not user_query:
                await message.answer("–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –∑–∞–¥–∞–π—Ç–µ –≤–æ–ø—Ä–æ—Å")
                return
            
            # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä "–ø–µ—á–∞—Ç–∞–µ—Ç"
            await bot_instance.bot.send_chat_action(
                chat_id=message.chat.id,
                action="typing"
            )
            
            # –ü–∞—Ä—Å–∏–º –∑–∞–ø—Ä–æ—Å –≤ SQL
            sql_query = bot_instance.nlp_parser.parse_query_to_sql(user_query)
            logger.info(f"–°–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω SQL: {sql_query}")
            
            # –í—ã–ø–æ–ª–Ω—è–µ–º –∑–∞–ø—Ä–æ—Å
            result = await bot_instance.db_ops.execute_query(sql_query)
            
            # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º –æ—Ç–≤–µ—Ç
            if result is None:
                response = "–ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –¥–∞–Ω–Ω—ã–µ"
            else:
                # –ò–∑–≤–ª–µ–∫–∞–µ–º —á–∏—Å–ª–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
                if isinstance(result, (list, tuple)) and len(result) > 0:
                    if isinstance(result[0], (list, tuple)) and len(result[0]) > 0:
                        value = result[0][0]
                    else:
                        value = result[0]
                else:
                    value = result
                
                # –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —á–∏—Å–ª–∞
                try:
                    if isinstance(value, (int, float)):
                        response = f"üìä –†–µ–∑—É–ª—å—Ç–∞—Ç: {value:,}".replace(',', ' ')
                    else:
                        response = f"üìä –†–µ–∑—É–ª—å—Ç–∞—Ç: {value}"
                except:
                    response = f"üìä –†–µ–∑—É–ª—å—Ç–∞—Ç: {value}"
            
            await message.answer(response)
            
        except Exception as e:
            logger.error(f"–û—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –∑–∞–ø—Ä–æ—Å–∞: {e}", exc_info=True)
            await message.answer("–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –∑–∞–ø—Ä–æ—Å–∞. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–µ—Ä–µ—Ñ–æ—Ä–º—É–ª–∏—Ä–æ–≤–∞—Ç—å –≤–æ–ø—Ä–æ—Å.")
    
    # –í–∫–ª—é—á–∞–µ–º —Ä–æ—É—Ç–µ—Ä –≤ –¥–∏—Å–ø–µ—Ç—á–µ—Ä
    dp.include_router(router)


